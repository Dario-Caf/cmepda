# Create an integer variable
this_is_an_int = 5
# Now check its type
print(type(this_is_an_int))

# Same with a string
this_is_a_string = 'Hello world' 
print(type(this_is_a_string))

# Same with a list
this_is_a_list = [1, 2, 3, 'stella']
print(type(this_is_a_list))

# Now a tuple
this_is_a_tuple = ('Frodo', 'Samvise', 'Meriadoc', 'Peregrino')
print(type(this_is_a_tuple))

# And finally a dict
this_is_a_dict = {'farina'     : '120g',
                  'latte'      : '170ml',
                  'uova'       : '1'}
print(type(this_is_a_dict))
